def parse_insights_scores(insights_content: str) -> dict:
    """
    Parse insights.md for score trends over time.
    Expected format: lines with scores (4-5 = high quality)
    Returns dict with topic counts by score level.
    """
    if not insights_content:
        return {"high_recent": 0, "medium_recent": 0, "low_recent": 0}

    lines = insights_content.split("\n")
    scores = {"5": 0, "4": 0, "3": 0, "2": 0, "1": 0}

    for line in lines:
        for score in scores.keys():
            if f"score: {score}" in line or f"Score: {score}" in line:
                scores[score] += 1

    return {
        "high_recent": scores["5"] + scores["4"],
        "medium_recent": scores["3"],
        "low_recent": scores["2"] + scores["1"],
        "raw_scores": scores
    }


def parse_hot_topics(hot_topics_content: str) -> list[str]:
    """Parse hot_topics.md for current interests."""
    if not hot_topics_content:
        return []

    topics = []
    lines = hot_topics_content.split("\n")

    for line in lines:
        line = line.strip()
        # Look for list items, headers, or topic keywords
        if line.startswith(("-", "*", "#")) and line.strip():
            # Extract text without markdown markers
            text = line.lstrip("-*# ").strip()
            if text and len(text) > 2:
                topics.append(text)

    return topics[:10]  # Top 10


def parse_concepts(concepts_content: str) -> list[str]:
    """Parse concepts.md for concepts being developed."""
    if not concepts_content:
        return []

    concepts = []
    lines = concepts_content.split("\n")

    for line in lines:
        line = line.strip()
        # Look for concept definitions or headers
        if line.startswith(("-", "*", "#")) or ":" in line:
            text = line.lstrip("-*# ").strip()
            if text and len(text) > 2:
                concepts.append(text)

    return concepts[:15]  # Top 15


