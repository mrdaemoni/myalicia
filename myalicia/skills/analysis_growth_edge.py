def read_memory_file(filename: str) -> str:
    """Read a memory file with graceful degradation."""
    filepath = MEMORY_FOLDER / filename
    if not filepath.exists():
        logger.warning(f"Memory file not found: {filepath}")
        return ""
    try:
        return filepath.read_text(encoding="utf-8")
    except Exception as e:
        logger.error(f"Error reading {filename}: {e}")
        return ""


def get_recent_synthesis_notes(days: int = 30, limit: int = 10) -> list[dict]:
    """Get the most recent synthesis notes within the specified number of days."""
    if not SYNTHESIS_FOLDER.exists():
        logger.warning(f"Synthesis folder not found: {SYNTHESIS_FOLDER}")
        return []

    cutoff_time = datetime.now() - timedelta(days=days)
    notes = []

    for filepath in SYNTHESIS_FOLDER.glob("*.md"):
        try:
            mtime = datetime.fromtimestamp(filepath.stat().st_mtime)
            if mtime >= cutoff_time:
                content = filepath.read_text(encoding="utf-8")
                notes.append({
                    "filename": filepath.name,
                    "mtime": mtime,
                    "content": content[:2000]  # First 2000 chars for context
                })
        except Exception as e:
            logger.error(f"Error reading synthesis note {filepath.name}: {e}")

    # Sort by mtime descending, return top limit
    notes.sort(key=lambda x: x["mtime"], reverse=True)
    return notes[:limit]


def append_to_analysis_insights(key: str, data: dict) -> None:
    """Append findings to analysis_insights.md with timestamp and source tag."""
    filepath = MEMORY_FOLDER / "analysis_insights.md"

    # Ensure directory exists
    MEMORY_FOLDER.mkdir(parents=True, exist_ok=True)

    # Prepare entry
    timestamp = datetime.now().isoformat()
    entry = f"""
## {key} - {timestamp}
Source: growth_edge_detection
Data: {json.dumps(data, indent=2)}

"""

    # Append to file
    try:
        if filepath.exists():
            current = filepath.read_text(encoding="utf-8")
            filepath.write_text(current + entry, encoding="utf-8")
        else:
            # Initialize file with header
            header = "# Analysis Insights\n\nAutogenerated by Alicia's Autonomous Analysis System\n"
            filepath.write_text(header + entry, encoding="utf-8")
        logger.info(f"Appended to analysis_insights.md: {key}")
    except Exception as e:
        logger.error(f"Error appending to analysis_insights.md: {e}")


def write_bridge_report(report_content: str) -> str:
    """Write detailed report to Bridge folder.

    Routes through bridge_protocol.write_bridge_text for atomicity +
    INDEX logging. (Was previously a bare `filepath.write_text(...)` —
    the migration also fixes the non-atomic write bug.)
    """
    date_str = datetime.now().strftime("%Y-%m-%d")
    report_filename = f"growth-edge-report-{date_str}.md"
    try:
        filepath = write_bridge_text(report_filename, report_content)
        logger.info(f"Wrote bridge report: {filepath}")
        return str(filepath)
    except Exception as e:
        logger.error(f"Error writing bridge report: {e}")
        return ""


