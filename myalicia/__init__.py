"""
myalicia — an AI teammate that grows into the shape of the person it serves.

Open-source pattern for relationship-shaped AI. The project is called *myalicia*
because every running instance is someone's own. The framework teaches you to
build *your* myalicia — shaped by you, by design.

myalicia is the tangible example of humorphism — the design philosophy that
says technology should take the shape of the human, not the other way around.
Where humorphism gives the philosophy, myalicia gives the architecture: three
relationship loops over a shared memory substrate.

The three loops:

    Listen   — seconds. Be present in the conversation in front of you.
    Notice   — minutes to hours. Catch the patterns across moments.
    Know     — days to weeks. Come to know the person, over time.

Each loop reads from and writes to a shared memory substrate (vault, notes,
archetype). Awareness compounds because three different cadences of reflection
are constantly metabolizing input into structure. That compounding is what
makes a teammate, not a tool.

Lineage and acknowledgments:

    - Garry Tan — for the "thin harness, fat skills" pattern that shaped the
      architecture of skill modules composed around a thin core.
    - Andrej Karpathy — for the autoresearch pattern that inspired the
      autonomous synthesis loops in Notice and Know.
    - Humorphism (humorphism.com) — the design philosophy this project embodies.

myalicia takes those primitives and applies them, seriously, to one human
relationship over time.

See PHILOSOPHY.md for the full framing. See README.md for setup.
See ACKNOWLEDGMENTS.md for the people whose work this builds on.
"""

