def analyze_with_sonnet(
    thinking_patterns: str,
    insights_analysis: dict,
    hot_topics: list[str],
    developed_concepts: list[str],
    synthesis_themes: str,
) -> dict:
    """
    Send analysis data to Sonnet for intelligent processing.
    Returns dict with growth edges, dormant threads, and suggested prompts.
    """
    if not ANTHROPIC_API_KEY:
        logger.error("ANTHROPIC_API_KEY not set")
        return {
            "growth_edges": [],
            "dormant_threads": [],
            "suggested_prompts": [],
            "error": "API key not configured"
        }

    client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY, max_retries=5)

    # Prepare analysis prompt
    prompt_text = f"""You are analyzing {USER_NAME}'s intellectual growth and thinking evolution.

CURRENT THINKING PATTERNS:
{thinking_patterns[:1000] if thinking_patterns else "No pattern data available"}

RECENT INSIGHT SCORES:
- High-scoring (4-5): {insights_analysis.get('high_recent', 0)} insights
- Medium (3): {insights_analysis.get('medium_recent', 0)} insights
- Low (1-2): {insights_analysis.get('low_recent', 0)} insights

CURRENT HOT TOPICS (Active Interests):
{chr(10).join(f"- {topic}" for topic in hot_topics[:8])}

DEVELOPED CONCEPTS (Existing Knowledge):
{chr(10).join(f"- {concept}" for concept in developed_concepts[:10])}

RECENT SYNTHESIS THEMES:
{synthesis_themes[:1500] if synthesis_themes else "No synthesis data available"}

ANALYSIS TASK:
1. Identify 2-4 GROWTH EDGES: Areas where {USER_NAME}'s thinking is actively evolving and expanding.
   - Where are novel insights emerging?
   - What connections are being made?
   - Which hot topics are NEW or underexplored in developed concepts?

2. Identify 2-4 DORMANT THREADS: Topics that were previously hot but have gone quiet.
   - What used to generate insights but isn't currently?
   - Are there abandoned exploration areas?

3. Suggest 3-5 FRONTIER PROMPTS that could open growth edges further.
   - Concrete, specific prompts that push existing edges
   - Should challenge and expand current thinking

Format your response as JSON with keys:
{{
  "growth_edges": [
    {{"area": "...", "evidence": "...", "why_expanding": "..."}},
    ...
  ],
  "dormant_threads": [
    {{"area": "...", "was_hot_because": "...", "reason_dormant": "..."}},
    ...
  ],
  "frontier_prompts": [
    "Specific, actionable prompt...",
    ...
  ],
  "analysis_summary": "2-3 sentence summary of overall intellectual trajectory"
}}"""

    try:
        message = client.messages.create(
            model=SONNET_MODEL,
            max_tokens=2000,
            messages=[
                {"role": "user", "content": prompt_text}
            ]
        )

        response_text = message.content[0].text

        # Parse JSON from response
        try:
            # Find JSON block in response
            start_idx = response_text.find("{")
            end_idx = response_text.rfind("}") + 1
            if start_idx >= 0 and end_idx > start_idx:
                json_str = response_text[start_idx:end_idx]
                return json.loads(json_str)
        except json.JSONDecodeError:
            logger.error(f"Failed to parse JSON from Sonnet response")
            return {
                "growth_edges": [],
                "dormant_threads": [],
                "frontier_prompts": [],
                "raw_response": response_text,
                "error": "JSON parsing failed"
            }

    except Exception as e:
        logger.error(f"Anthropic API error: {e}")
        return {
            "growth_edges": [],
            "dormant_threads": [],
            "frontier_prompts": [],
            "error": str(e)
        }


