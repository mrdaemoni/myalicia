def generate_bridge_report(
    analysis_result: dict,
    insights_analysis: dict,
    hot_topics: list[str],
    synthesis_notes: list[dict]
) -> str:
    """Generate detailed markdown report for the Bridge folder."""

    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    date_str = datetime.now().strftime("%Y-%m-%d")

    report = f"""# Growth Edge Analysis Report
**Date:** {timestamp}

## Executive Summary
{analysis_result.get('analysis_summary', 'Analysis summary not available')}

---

## Growth Edges (Active Expansion Areas)
Where {USER_NAME}'s thinking is actively evolving:

"""

    if analysis_result.get("growth_edges"):
        for i, edge in enumerate(analysis_result["growth_edges"], 1):
            report += f"""
### {i}. {edge.get('area', 'Unknown')}
- **Evidence:** {edge.get('evidence', 'N/A')}
- **Why Expanding:** {edge.get('why_expanding', 'N/A')}
"""
    else:
        report += "\nNo growth edges identified in current analysis.\n"

    report += "\n---\n\n## Dormant Threads (Previously Hot, Now Quiet)\n"

    if analysis_result.get("dormant_threads"):
        for i, thread in enumerate(analysis_result["dormant_threads"], 1):
            report += f"""
### {i}. {thread.get('area', 'Unknown')}
- **Was Hot Because:** {thread.get('was_hot_because', 'N/A')}
- **Reason for Dormancy:** {thread.get('reason_dormant', 'N/A')}
"""
    else:
        report += "\nNo dormant threads identified.\n"

    report += f"""
---

## Frontier Prompts (To Open Growth Edges Further)
These prompts could deepen and expand current thinking edges:

"""

    if analysis_result.get("frontier_prompts"):
        for i, prompt in enumerate(analysis_result["frontier_prompts"], 1):
            report += f"{i}. {prompt}\n\n"
    else:
        report += "No frontier prompts generated.\n\n"

    report += f"""
---

## Context Snapshot
**Analysis Date:** {date_str}

### Insight Scores (Recent)
- High-scoring (4-5 stars): {insights_analysis.get('high_recent', 0)}
- Medium (3 stars): {insights_analysis.get('medium_recent', 0)}
- Low (1-2 stars): {insights_analysis.get('low_recent', 0)}

### Current Hot Topics
{chr(10).join(f"- {topic}" for topic in hot_topics[:8])}

### Recent Synthesis Activity
{len(synthesis_notes)} synthesis notes reviewed from past 30 days:
"""

    for note in synthesis_notes[:5]:
        report += f"- {note['filename']} ({note['mtime'].strftime('%Y-%m-%d')})\n"

    report += """
---

## Methodology
This analysis uses:
1. Score trend analysis from insights database
2. Current interests (hot_topics.md)
3. Concept development progress (concepts.md)
4. Recent synthesis notes (past 30 days)
5. Sonnet LLM analysis to identify patterns and trajectories

Generated by: Alicia Autonomous Analysis System (Module 3)
"""

    return report


# ============================================================================
# MAIN ENTRY POINT
# ============================================================================

def run_growth_edge_detection() -> dict:
    """
    Main entry point for growth edge detection analysis.

    Returns:
        dict with keys:
            - status: "success" or "error"
            - growth_edges: list of identified growth edges
            - dormant_threads: list of dormant topics
            - suggested_prompts: list of frontier prompts
            - report_path: path to generated bridge report
            - timestamp: ISO timestamp of analysis
            - details: additional analysis details
    """

    logger.info("=" * 70)
    logger.info("Starting Growth Edge Detection (Module 3)")
    logger.info("=" * 70)

    start_time = datetime.now()
    timestamp = start_time.isoformat()

    # Step 1: Read memory files
    logger.info("Reading memory files...")
    patterns_content = read_memory_file("patterns.md")
    insights_content = read_memory_file("insights.md")
    hot_topics_content = read_memory_file("hot_topics.md")
    concepts_content = read_memory_file("concepts.md")

    # Step 2: Parse insights for score trends
    logger.info("Analyzing insight scores...")
    insights_analysis = parse_insights_scores(insights_content)
    logger.info(
        f"Insight summary: {insights_analysis['high_recent']} high, "
        f"{insights_analysis['medium_recent']} medium, "
        f"{insights_analysis['low_recent']} low"
    )

    # Step 3: Parse hot topics and concepts
    logger.info("Extracting hot topics and concepts...")
    hot_topics = parse_hot_topics(hot_topics_content)
    concepts = parse_concepts(concepts_content)
    logger.info(f"Found {len(hot_topics)} hot topics, {len(concepts)} concepts")

    # Step 4: Get recent synthesis notes
    logger.info("Reading recent synthesis notes...")
    synthesis_notes = get_recent_synthesis_notes(days=30, limit=10)
    synthesis_themes = "\n".join(
        f"- {note['filename']}: {note['content'][:200]}"
        for note in synthesis_notes
    )
    logger.info(f"Read {len(synthesis_notes)} synthesis notes")

    # Step 5: Send to Sonnet for analysis
    logger.info("Sending to Sonnet for analysis...")
    analysis_result = analyze_with_sonnet(
        thinking_patterns=patterns_content,
        insights_analysis=insights_analysis,
        hot_topics=hot_topics,
        developed_concepts=concepts,
        synthesis_themes=synthesis_themes,
    )

    if "error" in analysis_result and not analysis_result.get("growth_edges"):
        logger.error(f"Analysis failed: {analysis_result.get('error')}")
        return {
            "status": "error",
            "error": analysis_result.get("error"),
            "timestamp": timestamp,
            "growth_edges": [],
            "dormant_threads": [],
            "suggested_prompts": []
        }

    # Step 6: Generate bridge report
    logger.info("Generating bridge report...")
    report_content = generate_bridge_report(
        analysis_result,
        insights_analysis,
        hot_topics,
        synthesis_notes
    )
    report_path = write_bridge_report(report_content)

    # Step 7: Append to analysis_insights.md
    logger.info("Appending findings to analysis_insights.md...")
    append_to_analysis_insights("growth_edge", {
        "timestamp": timestamp,
        "growth_edges_count": len(analysis_result.get("growth_edges", [])),
        "dormant_threads_count": len(analysis_result.get("dormant_threads", [])),
        "frontier_prompts_count": len(analysis_result.get("frontier_prompts", [])),
        "report_path": report_path
    })

    # Step 8: Compile return dict
    elapsed = (datetime.now() - start_time).total_seconds()
    logger.info(f"Analysis complete in {elapsed:.1f}s")

    return {
        "status": "success",
        "timestamp": timestamp,
        "growth_edges": analysis_result.get("growth_edges", []),
        "dormant_threads": analysis_result.get("dormant_threads", []),
        "suggested_prompts": analysis_result.get("frontier_prompts", []),
        "analysis_summary": analysis_result.get("analysis_summary", ""),
        "report_path": report_path,
        "insights_analyzed": {
            "high_score_count": insights_analysis["high_recent"],
            "medium_score_count": insights_analysis["medium_recent"],
            "low_score_count": insights_analysis["low_recent"]
        },
        "data_sources": {
            "hot_topics_count": len(hot_topics),
            "concepts_count": len(concepts),
            "synthesis_notes_count": len(synthesis_notes)
        },
        "execution_time_seconds": elapsed
    }


